#include<iostream>
using namespace std;
main()
{
	int a,b,ans;
	
	cout<<"\nEnter A: ";
	cin>>a;
	
	cout<<"\nEnter B: ";
	cin>>b;
	
	ans = a+b;
	cout<<"\nAddition is : "<<ans;
	
	ans = a-b;
	cout<<"\nSubtraction is : "<<ans;
	
	ans = a*b;
	cout<<"\nMultiplication is : "<<ans;
	
	ans = a/b;
	cout<<"\nDivision is : "<<ans;
	
}
